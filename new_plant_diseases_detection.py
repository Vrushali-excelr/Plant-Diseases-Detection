train='/root/.cache/kagglehub/datasets/vipoooool/new-plant-diseases-dataset/versions/2/New Plant Diseases Dataset(Augmented)/New Plant Diseases Dataset(Augmented)/train'

size =224
train_generator=tf.keras.preprocessing.image.ImageDataGenerator(
    rotation_range=90,
    width_shift_range=0.0,
    height_shift_range=0.0,
    shear_range=0.0,
    horizontal_flip=True,
    vertical_flip=True,
    zoom_range=0.0,
    rescale=1./255,
    preprocessing_function=None,
    validation_split=0.1
).flow_from_directory(
    train,
    target_size=(size,size),
    batch_size=164,
    class_mode='categorical',
    color_mode='rgb',
    subset='training',
    shuffle=True,
)

train_generator.class_indices

classes=list(train_generator.class_indices.keys())
plt.figure(figsize=(20,20))
for x_batch,y_batch in train_generator:
    for i in range(0,16):
        plt.subplot(4,4,i+1)
        plt.imshow(x_batch[i])
        plt.imshow(x_batch[i])
        plt.title(classes[np.argmax(y_batch[i])])
        plt.grid(True)
    plt.tight_layout(
        pad=0.4,
        h_pad=0.5,
        w_pad=0.5,
        rect=(0, 0, 1, 0.9)
    )
    plt.show()
    break

